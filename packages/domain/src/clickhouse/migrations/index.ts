/**
 * A migration statement is either a raw SQL string (structural DDL) or a
 * structured {@link BackfillSpec} the apply engine can split into time-windowed
 * chunks (see `../backfill.ts`).
 */
export type MigrationStatement = string | BackfillSpec

export interface ClickHouseMigration {
	readonly version: number
	readonly description: string
	readonly statements: ReadonlyArray<MigrationStatement>
}

/**
 * Ordered list of every ClickHouse schema migration.
 *
 * The first entry is always the snapshot of the current schema (regenerated by
 * `bun run clickhouse:schema`). Subsequent entries are hand-authored deltas —
 * each one's `statements` should be the minimal set of `ALTER TABLE`,
 * `MATERIALIZE COLUMN`, or `CREATE MATERIALIZED VIEW` calls that take a server
 * from version N-1 to version N.
 *
 * A future improvement: a CI check that replays migrations 1..N against a fresh
 * ClickHouse and asserts the resulting `SHOW CREATE TABLE` matches running
 * just the latest snapshot. For now, snapshot tests on the DDL emitter plus
 * the live-DDL CI test catch the most common drift cases.
 */
export const migrations: ReadonlyArray<ClickHouseMigration> = [
	migration_0001_initial,
	migration_0002_service_map_edges_rollup,
	migration_0003_error_events_label,
	migration_0004_service_namespace_projections,
] as const
